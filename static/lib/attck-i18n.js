/**
 * Get Chinese name for an ATT&CK technique.
 * @param {string} techniqueId - e.g., "T1059.001"
 * @param {string} englishName - fallback English name
 * @returns {string} Chinese name or English name
 */
export function getTechniqueNameZh(techniqueId, englishName) {
  return TECHNIQUE_ZH[techniqueId] || TECHNIQUE_ZH[techniqueId.split('.')[0]] || englishName || techniqueId;
}

/**
 * Get Chinese name for an ATT&CK tactic.
 * @param {string} tactic - e.g., "initial-access"
 * @returns {string} Chinese name
 */
export function getTacticNameZh(tactic) {
  return TACTIC_ZH[tactic] || tactic;
}
